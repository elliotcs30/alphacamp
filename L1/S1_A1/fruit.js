// Step 1. 宣告變數: 將題目當中的資料儲存到變數當中
// 宣告變數: 各種水果的售價
let apple = 3
let orange = 4
let peach = 10

// 宣告變數: 各種水果的數量
let appleQuantity = 1
let orangeQuantity = 4
let peachQuantity = 2

// Step 2. 計算花費: 將你的數學邏輯轉換成程式邏輯: 用變數以及運算子計算出結果

let total = (apple * appleQuantity) + 
            (orange * orangeQuantity) + 
            (peach * peachQuantity)

// Step 3. 輸出結果
console.log(`${total}`)